    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if (jTableAvioes.getSelectedRow() > -1) {
            int codigo = Integer.parseInt(jTableAvioes
                    .getValueAt(jTableAvioes.getSelectedRow(), 0).toString());
            boolean del = new AviaoDAO().excluir(codigo);
            if (del) {
                popularListaAvioes();
                jButtonExcluir.setEnabled(false);
                jButtonEditar.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Aviao excluido");
            } else {
                JOptionPane.showMessageDialog(null, "Nao foi possivel apagar");
            }
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int linha = jTableAvioes.getSelectedRow();
        if (linha >= 0) {
            int codigo = Integer.parseInt(jTableAvioes
                    .getValueAt(jTableAvioes.getSelectedRow(), 0).toString());
            Aviao a = new AviaoDAO().getById(codigo);
            if (a == null) {
                JOptionPane.showMessageDialog(null, "Aviao nao existe mais");
            } else {
                new jFrameCadastroAviao(a).setVisible(true);
            }
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

