    private void popularListaAvioes() {
        ArrayList<Aviao> avioes = new AviaoDAO().list();
        DefaultTableModel model = (DefaultTableModel) jTableAvioes.getModel();
        model.setRowCount(0);
        for (Aviao a : avioes) {
            model.addRow(new Object[]{a.getId(), a.getNome(), a.getCategoria().getNome()});
        }

    }

    private void onClickTabela() {
        jTableAvioes.getSelectionModel().addListSelectionListener(
                new ListSelectionListener() {
                    @Override
                    public void valueChanged(ListSelectionEvent e) {
                        if (jTableAvioes.getSelectedRow() > -1) {
                            jButtonEditar.setEnabled(true);
                            jButtonExcluir.setEnabled(true);
                        } else {
                            jButtonEditar.setEnabled(false);
                            jButtonExcluir.setEnabled(false);
                        }
                    }
                });
    }

